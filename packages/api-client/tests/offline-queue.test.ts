describe('drainOutbox — replay loop (ADR-0044 §6, action items 4 & 5)', () => {
  it('replays all pending rows in FIFO order on success and marks each succeeded', async () => {
    const pending = [makeRecord('a'), makeRecord('b'), makeRecord('c')];
    const store = createReplayStore(pending);
    const seen: string[] = [];
    const replay = vi.fn(async (r: OutboxRecord) => {
      seen.push(r.idempotencyKey);
      return { ok: true };
    });

    const outcome = await drainOutbox({ store, replay });

    expect(outcome.kind).toBe('drained');
    expect(seen).toEqual(['a', 'b', 'c']);
    expect(store.succeeded).toEqual(['a', 'b', 'c']);
    expect(store.conflicts).toEqual([]);
  });

  it('aborts the run on ApiNetworkError, leaving the row pending (no conflict)', async () => {
    const pending = [makeRecord('a'), makeRecord('b')];
    const store = createReplayStore(pending);
    const replay = vi.fn(async (r: OutboxRecord) => {
      if (r.idempotencyKey === 'b') throw new ApiNetworkError('offline again');
      return { ok: true };
    });

    const outcome = await drainOutbox({ store, replay });

    expect(outcome.kind).toBe('aborted');
    expect(store.succeeded).toEqual(['a']);
    expect(store.conflicts).toEqual([]);
    // 'b' was NOT marked anything — it stays pending for the next online event.
  });

  it('aborts (not halts) on a 5xx — server trouble is transient', async () => {
    const store = createReplayStore([makeRecord('a')]);
    const replay = vi.fn(async () => {
      throw new ApiError({ code: 'INTERNAL_ERROR', message: 'boom', httpStatus: 503 });
    });

    const outcome = await drainOutbox({ store, replay });

    expect(outcome.kind).toBe('aborted');
    expect(store.conflicts).toEqual([]);
  });

  it('aborts (not halts) on an auth gap — no session to replay under', async () => {
    const store = createReplayStore([makeRecord('a')]);
    const replay = vi.fn(async () => {
      throw new ApiError({ code: 'UNAUTHORIZED', message: 'no session', httpStatus: 401 });
    });

    const outcome = await drainOutbox({ store, replay });

    expect(outcome.kind).toBe('aborted');
    expect(store.conflicts).toEqual([]);
    expect(store.succeeded).toEqual([]);
  });

  it('HALTS on a conflict: marks the row conflict, fires onConflict, and stops before later rows', async () => {
    const pending = [makeRecord('a'), makeRecord('bad'), makeRecord('c')];
    const store = createReplayStore(pending);
    const onConflict = vi.fn();
    const seen: string[] = [];
    const replay = vi.fn(async (r: OutboxRecord) => {
      seen.push(r.idempotencyKey);
      if (r.idempotencyKey === 'bad') {
        throw new ApiError({
          code: 'CLOSING_DAY_FINALIZED',
          message: 'Tagesabschluss locked',
          httpStatus: 409,
        });
      }
      return { ok: true };
    });

    const outcome = await drainOutbox({ store, replay, onConflict });

    expect(outcome.kind).toBe('halted');
    if (outcome.kind === 'halted') {
      expect(outcome.record.idempotencyKey).toBe('bad');
      expect(outcome.error).toBeInstanceOf(ApiOutboxConflictError);
      expect(outcome.error.serverCode).toBe('CLOSING_DAY_FINALIZED');
    }
    // 'a' succeeded, 'bad' marked conflict, 'c' never attempted.
    expect(seen).toEqual(['a', 'bad']);
    expect(store.succeeded).toEqual(['a']);
    expect(store.conflicts).toEqual(['bad']);
    expect(onConflict).toHaveBeenCalledTimes(1);
  });

  it('treats a generic 409 CONFLICT (STATE_DIVERGED) as a halt', async () => {
    const store = createReplayStore([makeRecord('x')]);
    const replay = vi.fn(async () => {
      throw new ApiError({ code: 'CONFLICT', message: 'diverged', httpStatus: 409 });
    });

    const outcome = await drainOutbox({ store, replay });

    expect(outcome.kind).toBe('halted');
    expect(store.conflicts).toEqual(['x']);
  });

  it('returns drained with succeeded=0 on an empty queue', async () => {
    const store = createReplayStore([]);
    const replay = vi.fn();
    const outcome = await drainOutbox({ store, replay });
    expect(outcome).toEqual({ kind: 'drained', succeeded: 0 });
    expect(replay).not.toHaveBeenCalled();
  });
});
